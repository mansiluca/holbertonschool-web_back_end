#!/usr/bin/env python3
import math
"""
This module contains a function that returns the floor of a float.
"""


def floor(n: float) -> int:
    """
    Return the largest integer less than or equal to n.

    Args:
        n: The float number to be floored

    Returns:
        The largest integer less than or equal to n
    """
    return math.floor(n)
