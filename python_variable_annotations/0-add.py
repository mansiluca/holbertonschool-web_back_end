#!/usr/bin/env python3
"""
This module contains a function that adds
two float numbers and returns the result as a float.
The function is annotated with type hints
to indicate the expected types of the arguments and the return value.
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers and return their sum as a float.

    Args:
        a: The first float number
        b: The second float number

    Returns:
        The sum of a and b as a float
    """
    return a + b
